package org.ehrscape.EhrscapeProvisioner;

public class SubObj {
	
	private String C;
	private String D;
	
	public SubObj(String c, String d) {
		this.C = c;
		this.D = d;
	}
	
	public void SetC(String C) {
		this.C = C;
	}
	
	public void SetD(String D) {
		this.D = D;
	}
	
	public String getC() {
		return C;
	}
	
	public String getD() {
		return D;
	}

}
